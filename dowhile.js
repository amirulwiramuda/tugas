var loop = ''
var loop2 = ''
var i = 2
var j = 20
var counter = 2


while (i <= 20) {
  loop += i + ' I love coding' + '\n'
  i += counter
}

while (j > 0) {
  loop2 += j + ' I love coding' + '\n'
  j -= counter
}
console.log('Looping pertama')
console.log(loop)
console.log('Looping kedua')
console.log(loop2)
